// tex-kernel-pep — loads and attaches the cgroup egress hooks (connect4/6,
// sendmsg4/6, sockops), points them at the local enforcement proxy, keeps the
// in-kernel verdict caches warm from the PDP so the hot FORBID set is blocked
// inline in microseconds, and serves the kernel-captured original destination
// to the userspace proxy over a UDS (the keystone the proxy needs to forward
// upstream on PERMIT).
//
// Build: see ../Makefile (clang -target bpf -> tex_redirect.o, then go build).
// Run:   as a privileged DaemonSet container (see ../deploy/daemonset.yaml).
//
// Authored against the fixed PDP contract: GET {PDP}/v1/govern/forbid-set to
// warm the caches; the proxy calls POST {PDP}/v1/govern/decide per request.

